package Entidad;

public class Rectangulo {
    private int base;
    private int alt;

    public Rectangulo() {
    }
    
    public Rectangulo(int base, int alt) {
        this.base = base;
        this.alt = alt;
    }

    public int getBase() {
        return base;
    }

    public int getAlt() {
        return alt;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public void setAlt(int alt) {
        this.alt = alt;
    }
 
}
